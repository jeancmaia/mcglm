def test_mc_id():
    id = [1, 1, 1]
    time = [1, 2, 3]

    df = pd.DataFrame([])

    df["id"] = id
    df["time"] = time

    z = mc_id(df)

    np.testing.assert_equal(z, np.array([[1, 0, 0], [0, 1, 0], [0, 0, 1]]))
